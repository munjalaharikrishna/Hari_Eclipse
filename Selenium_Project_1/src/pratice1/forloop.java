public class forloop {

	public static void main(String[] args) {
		
		
		for(int a = 1; a<=5; a++) {
			
			for(int m = 1; m<=a; m++ ) {
				
				if(m%2==0) {
					
					System.out.print("0");
				}
				else
				{
					System.out.print("1");
				}
				
		// System.out.print(m);	
				
		}
			System.out.println();
		}
		
		
		
		

	}

}
